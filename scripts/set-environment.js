#!/bin/node
const fs = require('fs');
const path = require('path');

const selectedEnv = process.argv[2];

if (!selectedEnv) {
  console.error(
    '💀💀💀 \x1b[47m\x1b[31mUsage: node scripts/set-environment.js <environment>\x1b[0m 💀💀💀'
  );
  process.exit(1);
}

const envFile = `.env.${selectedEnv}`;

if (!fs.existsSync(envFile)) {
  console.error(
    '💀💀💀 \x1b[47m\x1b[31mEnvironment file not found:',
    envFile,
    '\x1b[0m 💀💀💀'
  );
  console.error(
    'Available: .env.development, .env.staging, .env.production'
  );
  console.error(
    'Tip: Copy .env.example to create your environment files'
  );
  process.exit(1);
}

// Parse .env file (KEY=VALUE format)
const envVars = {};
const lines = fs.readFileSync(envFile, 'utf8').split('\n');
for (const line of lines) {
  const trimmed = line.trim();
  if (!trimmed || trimmed.startsWith('#')) continue;
  const eqIndex = trimmed.indexOf('=');
  if (eqIndex === -1) continue;
  const key = trimmed.slice(0, eqIndex).trim();
  let value = trimmed.slice(eqIndex + 1).trim();
  // Remove surrounding quotes if present
  if (
    (value.startsWith('"') && value.endsWith('"')) ||
    (value.startsWith("'") && value.endsWith("'"))
  ) {
    value = value.slice(1, -1);
  }
  envVars[key] = value;
}

const content = `/* eslint-disable */
/**************** DO NOT TOUCH *****************/
/* This file gets generated by 'env:*' scripts */
/***********************************************/

export default ${JSON.stringify(envVars, undefined, 2)};
`;

// Ensure the src/env directory exists
const envDir = path.join('src', 'env');
if (!fs.existsSync(envDir)) {
  fs.mkdirSync(envDir, { recursive: true });
}

fs.writeFileSync('src/env/env.js', content);
